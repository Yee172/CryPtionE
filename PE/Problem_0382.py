# A polygon is a flat shape consisting of straight line
# segments that are joined to form a closed chain or
# circuit. A polygon consists of at least three sides
# and does not self-intersect.
# A set S of positive numbers is said to generate a polygon
# P if:
#     no two sides of P are the same length,
#     the length of every side of P is in S, and
#     S contains no other value.
# Consider the sequence s, defined as follows:
#     s_{1} = 1, s_{2} = 2, s_{3} = 3
#     s_{n} = s_{n - 1} + s_{n - 3} for n > 3.
# Let U_{n} be the set {s_{1}, s_{2}, \ldots, s_{n}}.
# Let f(n) be the number of subsets of U_{n} which generate
# at least one polygon.
# Find the last 9 digits of f(10^{18}).
# ----------------------------------------------------
# Analysis: brute force

