#     (\sqrt{2} + \sqrt{3})^{14} = 9313929.99999989263 \cdots
#     (\sqrt{2} + \sqrt{3})^{16} = 92198401.99999998915 \cdots
# It looks as if the number of consecutive nines at the
# beginning of the fractional part of these powers is
# non-decreasing.
# In fact it can be proven that the fractional part of
# (\sqrt{2} + \sqrt{3})^{2 n} approaches 1 for large n.
