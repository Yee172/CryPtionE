# chain of connected primes between 2 and P and no prime
# in the chain exceeds P.
# For example, 127 is a 2's relative.
# However, 11 and 103 are not 2's relatives.
# Let F(N) be the sum of the primes \leq N which are
# not 2's relatives.
# Find F(10^{7}).
# ----------------------------------------------------
