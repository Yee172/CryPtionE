# Such a sum is called the Zeckendorf representation
# of the number.
# For any integer n > 0, let z(n) be the number of terms
# in the Zeckendorf representation of n.
# Find \sum z(n) for 0 < n < 10^{17}.
# ----------------------------------------------------
