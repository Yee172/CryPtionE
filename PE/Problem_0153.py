# As we all know the equation x^{2} = -1 has no solutions
# for real x.
# If we however introduce the imaginary number i this
# equation has two solutions: x = i and x = -i.
# If we go a step further the equation (x - 3)^{2} = -4
# has two complex solutions: x = 3 + 2 i and x = 3 - 2 i.
# x = 3 + 2 i and x = 3 - 2 i are called each others'
# complex conjugate.
# Numbers of the form a + b i are called complex numbers.
# In general a + b i and a − b i are each other's complex
# conjugate.
# A Gaussian Integer is a complex number a + b i such that
# both a and b are integers.
# The regular integers are also Gaussian integers (with b = 0).
# To distinguish them from Gaussian integers with b \neq 0
# we call such integers "rational integers."
# A Gaussian integer is called a divisor of a rational
# integer n if the result is also a Gaussian integer.
# Note also that if the Gaussian Integer (a + b i) is a
# divisor of a rational integer n, then its complex conjugate
# (a − b i) is also a divisor of n.
# In fact, 5 has six divisors such that the real part is
# positive: {1, 1 + 2 i, 1 − 2 i, 2 + i, 2 − i, 5}.
# What is \sum_{n = 1}^{10^{8}} s(n)?
# ----------------------------------------------------
