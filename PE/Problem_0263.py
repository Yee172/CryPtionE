# A number n is called a practical number if every number
# from 1 up to and including n can be expressed as a sum
# of distinct divisors of n.
# A pair of consecutive prime numbers with a difference
# of six is called a sexy pair (since "sex" is the Latin
# word for "six").
# We may occasionally find a triple-pair, which means three
# consecutive sexy prime pairs, such that the second member
# of each pair is the first member of the next pair.
# We shall call a number n such that:
#     (n - 9, n - 3), (n - 3, n + 3), (n + 3, n + 9) form a triple-pair, and
#     the numbers n - 8, n - 4, n, n + 4 and n + 8 are all practical,
# an engineers' paradise.
# Find the sum of the first four engineers' paradises.
# ----------------------------------------------------
# Analysis: brute force
#           Hint: https://en.wikipedia.org/wiki/Practical_number

