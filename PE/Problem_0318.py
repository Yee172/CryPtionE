# the beginning of the fractional part of
# (\sqrt{p} + \sqrt{q})^{2 n}.
# Let N(p, q) be the minimal value of n such that
# C(p, q, n) \geq 2011.
# Find \sum N(p, q) for p + q \leq 2011.
